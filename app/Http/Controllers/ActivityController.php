    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return RedirectResponse
     */
    public function destroy(int $id)
    {
        $activity = Activity::findOrFail($id);

        try {
            $activity->delete();
        } catch (\Exception $e) {
            return back()
                ->withErrors(__('model.activity.not_deleted'));
        }

        return redirect()
            ->route('activities')
            ->with('success', __('model.activity.deleted'));
    }
