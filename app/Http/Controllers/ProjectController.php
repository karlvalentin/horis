    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $project = Project::findOrFail($id);

        try {
            $project->delete();
        } catch (\Exception $e) {
            return back()
                ->withErrors(__('model.project.not_deleted'));
        }

        return redirect()
            ->route('projects')
            ->with('success', __('model.project.deleted'));
    }
