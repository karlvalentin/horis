    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\RedirectResponse
     */
    public function destroy(int $id)
    {
        $customer = Customer::findOrFail($id);

        try {
            $customer->delete();
        } catch (\Exception $e) {
            return back()
                ->withErrors(__('model.customer.not_deleted'));
        }

        return redirect()
            ->route('customers')
            ->with('success', __('model.customer.deleted'));
    }
